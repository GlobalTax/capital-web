<h2>Fórmula del EBITDA: cómo calcularlo paso a paso</h2>

<p>Existen dos formas de calcular el EBITDA a partir de la cuenta de resultados:</p>

<h3>Método 1: desde el resultado neto (bottom-up)</h3>

<p>Partiendo del beneficio neto, se suman de vuelta todas las partidas que se descontaron:</p>

