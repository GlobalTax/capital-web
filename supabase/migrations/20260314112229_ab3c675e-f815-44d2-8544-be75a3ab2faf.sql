<h2>Preguntas frecuentes sobre el EBITDA</h2>

<h3>¿Cuál es un buen margen EBITDA para una empresa española?</h3>

<p>Depende del sector. En tecnología y software, un margen EBITDA superior al 20-25% es habitual. En servicios profesionales, entre el 15-25%. En manufactura y retail, del 8-15%. Por debajo del 10% en la mayoría de sectores dificulta la financiación y el atractivo para compradores.</p>

<h3>¿Se puede vender una empresa con EBITDA negativo?</h3>

<p>Sí, pero es mucho más difícil y el precio se basa en otras métricas como facturación, ARR (ingresos recurrentes anuales) o potencial de mercado. Ocurre principalmente en startups tecnológicas de alto crecimiento o empresas en turnaround.</p>

<h3>¿Cuántos años de EBITDA se usan para valorar una empresa?</h3>

<p>Lo habitual en operaciones M&amp;A es usar el EBITDA de los últimos 12 meses (LTM o trailing twelve months) ajustado por partidas no recurrentes, o una media ponderada de los últimos 2-3 años si el negocio tiene estacionalidad o irregularidades.</p>

<h3>¿El EBITDA aparece directamente en la cuenta de resultados?</h3>

<p>No. La cuenta de resultados española (PGC) no presenta el EBITDA como línea separada. Hay que calcularlo sumando al resultado de explotación (EBIT) las amortizaciones y depreciaciones que aparecen en la memoria o en el desglose del resultado operativo.</p>

<h3>¿Qué diferencia hay entre EBITDA y flujo de caja libre?</h3>

<p>El EBITDA es una métrica contable aproximada. El flujo de caja libre (FCF) es la caja real disponible para accionistas y deudores, después de descontar el CAPEX y las variaciones en capital circulante. FCF = EBITDA – CAPEX – variación de working capital – impuestos pagados.</p>

