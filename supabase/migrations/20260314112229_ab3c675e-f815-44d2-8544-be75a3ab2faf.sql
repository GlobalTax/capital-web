<h2>EBITDA ajustado o normalizado: qué es y por qué importa en M&amp;A</h2>

<p>En una operación de compraventa, no se trabaja con el EBITDA contable tal cual aparece en las cuentas, sino con el <strong>EBITDA ajustado</strong> (también llamado EBITDA normalizado o recurrente). Este ajuste elimina partidas extraordinarias que distorsionan la imagen del negocio:</p>

<ul><li><strong>Retribución del socio directivo:</strong> Salario del propietario por encima o por debajo del mercado</li><li><strong>Gastos no recurrentes:</strong> Gastos personales del empresario cargados a la empresa (coche, viajes, seguros)</li><li><strong>Costes one-time:</strong> Gastos de asesoría o consultoría extraordinarios no repetibles</li><li><strong>Operaciones con partes vinculadas:</strong> Alquileres entre partes vinculadas a precios fuera de mercado</li><li><strong>Provisiones:</strong> Provisiones extraordinarias o reversiones de provisiones</li></ul>

<p>El EBITDA ajustado suele ser superior al EBITDA contable porque se eliminan gastos que no continuarán tras la venta. Por eso es el punto de partida de cualquier negociación seria de precio en una operación M&amp;A.</p>

<h2>Limitaciones del EBITDA: cuándo no es suficiente</h2>

<p>El EBITDA es una métrica muy útil, pero tiene limitaciones importantes que los empresarios deben conocer:</p>

<ul><li><strong>Ignora el CAPEX:</strong> No tiene en cuenta las necesidades de inversión (CAPEX). Una empresa industrial que necesita invertir 500.000 € al año en maquinaria tiene un flujo de caja real muy inferior a su EBITDA.</li><li><strong>No captura el working capital:</strong> No refleja las variaciones en el capital circulante (cobros de clientes, pagos a proveedores, stocks). Un negocio con mucho crecimiento puede tener EBITDA alto pero necesidades de caja elevadas.</li><li><strong>Las amortizaciones son un coste real:</strong> En sectores con activos intensivos (manufactura, transporte, inmobiliario), las amortizaciones son un coste real significativo. El EBITDA puede sobreestimar la rentabilidad.</li><li><strong>Susceptible de ajustes agresivos:</strong> Puede manipularse mediante decisiones contables sobre qué partidas son ordinarias o extraordinarias.</li></ul>

<p>Por eso en <strong>due diligence financiera</strong> se analiza siempre el EBITDA junto con el Free Cash Flow (flujo de caja libre) y el CAPEX, que ofrecen una imagen más completa de la salud financiera del negocio.</p>

