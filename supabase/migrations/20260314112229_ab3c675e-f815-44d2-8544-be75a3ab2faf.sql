<h2>Ejemplo práctico de cálculo del EBITDA</h2>

<p>Supongamos una empresa española de servicios industriales con los siguientes datos de su cuenta de resultados anual:</p>

<table><thead><tr><th>Concepto</th><th>Importe</th></tr></thead><tbody><tr><td>Ventas netas</td><td>2.000.000 €</td></tr><tr><td>Coste de ventas (COGS)</td><td>– 800.000 €</td></tr><tr><td>Margen bruto</td><td>1.200.000 €</td></tr><tr><td>Gastos operativos (personal, alquiler, marketing)</td><td>– 700.000 €</td></tr><tr><td><strong>EBITDA</strong></td><td><strong>500.000 €</strong></td></tr><tr><td>Amortizaciones</td><td>– 80.000 €</td></tr><tr><td>EBIT (resultado operativo)</td><td>420.000 €</td></tr><tr><td>Intereses y gastos financieros</td><td>– 20.000 €</td></tr><tr><td>EBT (resultado antes de impuestos)</td><td>400.000 €</td></tr><tr><td>Impuesto de Sociedades (25%)</td><td>– 100.000 €</td></tr><tr><td>Resultado neto</td><td>300.000 €</td></tr></tbody></table>

<p>En este ejemplo, el EBITDA es de <strong>500.000 €</strong>, lo que supone un <strong>margen EBITDA del 25%</strong> sobre ventas (500.000 / 2.000.000). El resultado neto es de 300.000 €, pero el EBITDA es un 67% superior porque devuelve al cálculo las amortizaciones (80.000 €) y los intereses financieros (20.000 €), que no son costes operativos del negocio en sí.</p>

<blockquote><p><strong>¿Por qué el EBITDA es mayor que el beneficio neto?</strong> Porque el EBITDA elimina partidas contables (amortizaciones) y de estructura financiera (intereses e impuestos) que no reflejan la operativa real del negocio. Un comprador o inversor quiere saber cuánta caja genera el negocio antes de decisiones de financiación — eso es el EBITDA.</p></blockquote>

<h2>Para qué sirve el EBITDA: sus principales usos</h2>

<h3>1. Valoración de empresas en operaciones M&amp;A</h3>

<p>El EBITDA es la base de la <strong>metodología de valoración por múltiplos</strong>, la más utilizada en operaciones de compraventa de empresas en el mid-market español. El valor de la empresa se calcula multiplicando el EBITDA por el múltiplo que corresponde a su sector:</p>

<p><strong>Valor de empresa (EV) = EBITDA × Múltiplo sectorial</strong></p>

<p>Por ejemplo, una empresa de tecnología con EBITDA de 1.000.000 € y un múltiplo sectorial de 6,8x tendría un Enterprise Value estimado de 6.800.000 €.</p>

<h3>2. Comparar empresas del mismo sector</h3>

<p>Como el EBITDA elimina el efecto de la estructura de capital y la fiscalidad, permite comparar de forma objetiva la eficiencia operativa de empresas distintas: una con mucha deuda y una sin deuda, o una que amortiza rápido y otra que no.</p>

<h3>3. Análisis de capacidad de pago de deuda</h3>

<p>Los bancos y fondos de deuda usan el ratio <strong>Deuda Neta / EBITDA</strong> para evaluar si una empresa puede devolver su deuda. Un ratio inferior a 3x se considera saludable en el mid-market español; por encima de 5x empieza a ser preocupante.</p>

<h3>4. Seguimiento de la evolución del negocio</h3>

<p>Los empresarios usan el EBITDA mensual o trimestral para monitorizar la salud operativa del negocio con independencia de los ciclos de inversión, la amortización de activos o los cambios en la financiación bancaria.</p>

