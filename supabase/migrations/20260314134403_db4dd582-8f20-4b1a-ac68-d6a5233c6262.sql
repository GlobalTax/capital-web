<h2>Definición concisa de Due Diligence</h2>
<blockquote><p><strong>Due diligence</strong> significa literalmente ''diligencia debida'' en español. Es la investigación detallada que realiza el comprador sobre los aspectos financieros, legales, fiscales y operativos de una empresa antes de cerrar su adquisición. El resultado es un informe de hallazgos que sirve de base para negociar el precio final y las garantías del contrato.</p></blockquote>

<h2>Due diligence: significado y origen del término</h2>
<p>El término proviene del latín <em>''diligentia debita''</em> y fue adoptado en el mundo financiero anglosajón en los años 30 del siglo XX. En castellano se traduce como ''diligencia debida'' o ''auditoría de compra'', aunque en la práctica profesional española se usa casi universalmente el término en inglés.</p>
<p>En el contexto legal, la due diligence hace referencia a la obligación del comprador de investigar con rigor antes de cerrar una transacción. Si el comprador no realiza una due diligence adecuada y después descubre problemas en la empresa adquirida, tiene menos argumentos legales para reclamar al vendedor.</p>

<h3>Due diligence en español</h3>
<p>''Due diligence'' se traduce como <strong>''diligencia debida''</strong> o <strong>''auditoría de compra''</strong>. En España y Latinoamérica, los profesionales de M&amp;A utilizan habitualmente el término inglés. También se abrevia como ''DD'' en documentos internos y comunicaciones entre asesores.</p>

<h2>Para qué sirve la due diligence: sus tres funciones principales</h2>

<h3>1. Verificar la información del vendedor</h3>
<p>El vendedor presenta sus mejores datos en el proceso de venta. La due diligence contrasta esa información con documentación real: extractos bancarios, contratos originales, registros contables auditados, actas del consejo, expedientes laborales. El objetivo es confirmar que lo que se ha presentado en el teaser y en el cuaderno de venta es real.</p>

<h3>2. Identificar riesgos y contingencias ocultas</h3>
<p>Toda empresa tiene riesgos. La due diligence los cuantifica y clasifica para que el comprador pueda decidir si asumirlos, negociar garantías o ajustar el precio. Los riesgos más habituales en el mid-market español son: litigios laborales pendientes, deudas fiscales no provisionadas, contratos de clientes próximos a vencer y dependencia excesiva del propietario.</p>

<h3>3. Determinar el precio justo y las garantías</h3>
<p>El resultado de la due diligence alimenta directamente la negociación del precio. Los hallazgos se traducen en ajustes de precio (<em>price chips</em>), garantías en el contrato de compraventa (SPA) o retenciones del precio en un escrow. Sin due diligence, el comprador negocia a ciegas.</p>

<h2>Tipos de due diligence en una operación M&amp;A</h2>

<h3>Due diligence financiero</h3>
<p>Es el análisis más importante y el que mayor impacto tiene en la valoración. El equipo financiero revisa en profundidad las cuentas de los últimos 3-5 años para obtener el <strong>EBITDA ajustado real</strong>, la <strong>deuda neta</strong> y el <strong>working capital normalizado</strong> — los tres parámetros que determinan el precio de la operación.</p>

<h3>Due diligence legal</h3>
<p>Los abogados revisan la estructura societaria, los contratos clave (clientes, proveedores, arrendamientos, financiación), los litigios en curso, la propiedad intelectual y las licencias necesarias para operar. El objetivo es identificar pasivos contingentes que no aparecen en el balance.</p>

<h3>Due diligence fiscal</h3>
<p>Los asesores fiscales analizan la situación tributaria de los últimos 4 años no prescritos: declaraciones del IS, IVA, retenciones, posibles inspecciones pendientes y planificaciones fiscales agresivas que podrían ser cuestionadas por Hacienda. En España, el riesgo fiscal es una de las contingencias más habituales en operaciones mid-market.</p>

<h3>Due diligence laboral</h3>
<p>Se revisa la plantilla: contratos vigentes, convenio colectivo aplicable, retribuciones, beneficios sociales, ERTEs pasados, posibles conflictos sindicales y pasivos por indemnizaciones. También se analiza la dependencia de personas clave (<em>key men</em>) cuya salida podría afectar al negocio.</p>

<h3>Due diligence comercial</h3>
<p>Análisis del mercado, posición competitiva, cartera de clientes (concentración, contratos, renovaciones) y proveedores principales. El objetivo es validar que el crecimiento presentado por el vendedor es sostenible y que no hay riesgos comerciales inminentes.</p>

<h3>Vendor Due Diligence (VDD)</h3>
<p>La <strong>Vendor Due Diligence</strong> es una due diligence encargada y pagada por el propio vendedor antes de iniciar el proceso de venta. Un asesor independiente analiza la empresa y emite un informe que se comparte con todos los potenciales compradores. Acelera el proceso, aumenta la credibilidad del vendedor y reduce el coste total del proceso al evitar múltiples due diligences paralelas.</p>

<h2>Fases de la due diligence: cómo se desarrolla el proceso</h2>
<p>En una operación de compraventa de empresa típica en el mid-market español (€5M–€100M EV), la due diligence se desarrolla en 6 fases:</p>

<table>
<thead><tr><th>Fase</th><th>Áreas analizadas</th><th>Duración típica</th></tr></thead>
<tbody>
<tr><td><strong>Fase 1</strong></td><td>Análisis preliminar: revisión de cuentas anuales, estructura societaria, contratos principales</td><td>1–2 semanas</td></tr>
<tr><td><strong>Fase 2</strong></td><td>Due diligence financiero: EBITDA ajustado, deuda neta, working capital, calidad del resultado</td><td>2–3 semanas</td></tr>
<tr><td><strong>Fase 3</strong></td><td>Due diligence legal: contratos, litigios, propiedad intelectual, régimen de garantías</td><td>2–3 semanas</td></tr>
<tr><td><strong>Fase 4</strong></td><td>Due diligence fiscal: posición fiscal, inspecciones pendientes, contingencias tributarias</td><td>1–2 semanas</td></tr>
<tr><td><strong>Fase 5</strong></td><td>Due diligence laboral y comercial: plantilla, clientes, proveedores, posición competitiva</td><td>1–2 semanas</td></tr>
<tr><td><strong>Fase 6</strong></td><td>Informe de hallazgos y negociación de ajustes de precio o garantías</td><td>1 semana</td></tr>
</tbody>
</table>

<p>El proceso comienza tras la firma de la <strong>Carta de Intenciones (LOI)</strong> y se desarrolla a través de un <em>data room</em> virtual donde el vendedor deposita toda la documentación solicitada. El comprador y sus asesores acceden al data room y van enviando preguntas (Q&amp;A) al equipo del vendedor.</p>

<h2>Due diligence financiero: qué se analiza en detalle</h2>
<p>El due diligence financiero es el corazón del proceso. Estos son los principales bloques de análisis:</p>

<table>
<thead><tr><th>Área de análisis</th><th>Qué se revisa</th></tr></thead>
<tbody>
<tr><td><strong>Calidad del EBITDA</strong></td><td>Identificar partidas no recurrentes que inflan el resultado (ingresos extraordinarios, subvenciones, reversiones de provisiones)</td></tr>
<tr><td><strong>Deuda neta ajustada</strong></td><td>Verificar toda la deuda financiera: préstamos bancarios, leasing, deudas con socios, pasivos contingentes</td></tr>
<tr><td><strong>Working capital normalizado</strong></td><td>Analizar el ciclo de cobro/pago y el nivel de stocks para determinar el capital circulante necesario</td></tr>
<tr><td><strong>CAPEX histórico y futuro</strong></td><td>Distinguir CAPEX de mantenimiento (recurrente) de CAPEX de crecimiento (no recurrente)</td></tr>
<tr><td><strong>Calidad de los ingresos</strong></td><td>Recurrencia, concentración en clientes, contratos en vigor, visibilidad futura del pipeline</td></tr>
<tr><td><strong>Contingencias ocultas</strong></td><td>Avales, garantías prestadas, litigios con impacto patrimonial, deudas fiscales no provisionadas</td></tr>
</tbody>
</table>

<p>El resultado del due diligence financiero es el <strong>EBITDA ajustado normalizado</strong> — que puede diferir significativamente del EBITDA contable — y la <strong>deuda neta ajustada</strong>, que determina el equity value final de la operación.</p>

<blockquote><p><strong>¿Cuál es la diferencia entre EBITDA contable y EBITDA ajustado?</strong><br>El EBITDA contable es el que aparece directamente en la cuenta de resultados. El EBITDA ajustado elimina partidas no recurrentes: el salario del propietario por encima de mercado, gastos personales cargados a la empresa, ingresos extraordinarios, costes one-time de reestructuración, etc. En el mid-market español, la diferencia entre ambos puede ser del 20-40%.</p></blockquote>

<h2>Checklist de due diligence: documentación habitual en el data room</h2>
<p>Esta es la documentación que habitualmente se solicita en una due diligence de una pyme española:</p>

<h3>Información corporativa y societaria</h3>
<ul>
<li>Escrituras de constitución y estatutos sociales actualizados</li>
<li>Libro de actas del Consejo de Administración y Junta General (últimos 5 años)</li>
<li>Estructura accionarial completa y pactos de socios si los hubiera</li>
<li>Poderes notariales vigentes</li>
</ul>

<h3>Información financiera</h3>
<ul>
<li>Cuentas anuales auditadas de los últimos 3-5 ejercicios</li>
<li>Modelo de negocio y presupuesto del ejercicio en curso</li>
<li>Extractos bancarios de los últimos 12 meses</li>
<li>Detalle de deuda financiera: préstamos, leasing, pólizas de crédito</li>
<li>Aging de clientes y proveedores (antigüedad de saldos)</li>
</ul>

<h3>Contratos y relaciones comerciales</h3>
<ul>
<li>Contratos de los 10 clientes principales (importe, duración, condiciones de renovación)</li>
<li>Contratos con proveedores estratégicos</li>
<li>Contratos de arrendamiento de locales y equipos</li>
<li>Contratos de distribución, licencias y franquicias si aplica</li>
</ul>

<h3>Información fiscal y laboral</h3>
<ul>
<li>Declaraciones del IS, IVA y retenciones de los últimos 4 años</li>
<li>Actas de inspecciones fiscales si las hubiera</li>
<li>Relación completa de empleados: categoría, antigüedad, retribución</li>
<li>Convenio colectivo aplicable y acuerdos de empresa</li>
</ul>

<h3>Activos e inmuebles</h3>
<ul>
<li>Inventario de activos fijos y valor contable</li>
<li>Registros de propiedad de inmuebles propios</li>
<li>Pólizas de seguros vigentes</li>
<li>Registro de marcas, patentes y propiedad intelectual</li>
</ul>

<h2>Errores más comunes en la due diligence que cuestan dinero</h2>

<p><strong>No revisar los contratos de clientes.</strong> Muchas empresas presentan una base de clientes sólida, pero sin contratos firmados o con contratos que vencen próximamente. Descubrirlo tarde puede suponer un ajuste de precio significativo.</p>

<p><strong>Ignorar los pasivos laborales.</strong> Los ERTEs mal gestionados, los falsos autónomos o los acuerdos verbales con empleados clave son fuente habitual de contingencias en el mid-market español.</p>

<p><strong>Aceptar el EBITDA del vendedor sin ajustarlo.</strong> El EBITDA presentado en el teaser casi siempre incluye add-backs agresivos. La due diligence financiera debe validar cada ajuste con documentación.</p>

<p><strong>No revisar la concentración de clientes.</strong> Una empresa con el 60% de facturación en un solo cliente tiene un riesgo muy diferente a una empresa diversificada, aunque el EBITDA sea el mismo.</p>

<p><strong>Subestimar el working capital necesario.</strong> El precio se negocia sobre deuda neta pero el working capital locked-box puede generar disputas post-cierre importantes si no se define bien en el SPA.</p>

<h2>¿Cuánto dura y cuánto cuesta una due diligence?</h2>
<p>La duración depende del tamaño y complejidad de la empresa. En el mid-market español (€5M–€100M EV):</p>
<ul>
<li><strong>Due diligence simplificada</strong> (empresa pequeña, &lt;€10M EV): 3-4 semanas, coste de asesoría entre €15.000 y €40.000</li>
<li><strong>Due diligence estándar</strong> (€10M–€50M EV): 5-8 semanas, coste entre €40.000 y €100.000</li>
<li><strong>Due diligence compleja</strong> (&gt;€50M EV o sector regulado): 8-12 semanas, coste superior a €100.000</li>
</ul>

<blockquote><p>El coste de la due diligence es siempre una inversión, no un gasto. Una due diligence bien ejecutada puede identificar contingencias que justifican una reducción del precio de compra varias veces superior a su coste.</p></blockquote>

<h3>Vendor Due Diligence: cuándo vale la pena</h3>
<p>Si el vendedor espera recibir ofertas de varios compradores en un proceso competitivo, la Vendor Due Diligence reduce el tiempo total del proceso (de 8-12 semanas a 3-4 semanas por comprador) y aumenta la credibilidad del proceso. En operaciones por encima de €20M EV en España, la VDD se ha convertido en práctica estándar.</p>

<h2>Preguntas frecuentes sobre la due diligence</h2>

<h3>¿Quién paga la due diligence, el comprador o el vendedor?</h3>
<p>En una due diligence estándar (buy-side), la paga el comprador porque analiza la empresa que quiere adquirir. En la Vendor Due Diligence (VDD), la paga el vendedor porque es él quien encarga el informe para facilitarlo a los compradores. Los costes de asesoría legal y financiera del vendedor los asume el vendedor.</p>

<h3>¿Se puede comprar una empresa sin hacer due diligence?</h3>
<p>Legalmente es posible, pero altamente arriesgado. Sin due diligence, el comprador asume todos los pasivos ocultos de la empresa y tiene muy poca capacidad de reclamar al vendedor por problemas descubiertos tras el cierre. En la práctica, ningún asesor profesional recomienda cerrar una operación sin due diligence.</p>

<h3>¿Cuál es la diferencia entre due diligence y auditoría?</h3>
<p>La auditoría es un proceso periódico que verifica que las cuentas anuales presentan la imagen fiel de la empresa según las normas contables (PGC en España). La due diligence es un análisis puntual orientado a una transacción específica, que va mucho más allá de las cuentas: analiza contratos, riesgos legales, posición fiscal, calidad del EBITDA y sostenibilidad del negocio.</p>

<h3>¿Qué es un data room en due diligence?</h3>
<p>Un data room es un repositorio digital seguro donde el vendedor deposita toda la documentación de la empresa para que el comprador y sus asesores puedan revisarla. Plataformas habituales en el mercado español son Intralinks, Ansarada, Datasite o simplemente SharePoint/Google Drive para operaciones pequeñas. El acceso se controla y queda registrado.</p>

<h3>¿Cuándo empieza la due diligence en el proceso de compraventa?</h3>
<p>La due diligence comienza normalmente tras la firma de la <strong>Carta de Intenciones (LOI)</strong>, que establece el precio indicativo, las condiciones principales y un período de exclusividad para el comprador. Antes de la LOI puede existir una revisión preliminar de información limitada, pero el proceso formal de due diligence arranca con exclusividad.</p>

